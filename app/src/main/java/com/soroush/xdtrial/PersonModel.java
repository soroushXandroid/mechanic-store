package com.soroush.xdtrial;

public class PersonModel {

    private int id, fPelak, lPelak, iranPelak;
    private String name, family, phone, ch,
            brand, model, color, gear, oil, date;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getfPelak() {
        return fPelak;
    }

    public void setfPelak(int fPelak) {
        this.fPelak = fPelak;
    }

    public int getlPelak() {
        return lPelak;
    }

    public void setlPelak(int lPelak) {
        this.lPelak = lPelak;
    }

    public int getIranPelak() {
        return iranPelak;
    }

    public void setIranPelak(int iranPelak) {
        this.iranPelak = iranPelak;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCh() {
        return ch;
    }

    public void setCh(String ch) {
        this.ch = ch;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getGear() {
        return gear;
    }

    public void setGear(String gear) {
        this.gear = gear;
    }

    public String getOil() {
        return oil;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public void setOil(String oil) {
        this.oil = oil;
    }
}
